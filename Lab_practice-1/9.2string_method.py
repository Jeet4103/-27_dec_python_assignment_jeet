text = "  Hello, Python World!  "

print("Original string:", text)
print("Lowercase:", text.lower())
print("Uppercase:", text.upper())
print("Strip whitespace:", text.strip())
print("Replace 'Python' with 'Programming':", text.replace("Python", "Programming"))
print("Split into a list:", text.split())
print("Check if string starts with 'Hello':", text.startswith("Hello"))
print("Check if string ends with 'World!':", text.endswith("World!"))
print("Count occurrences of 'o':", text.count("o"))
print("Find index of 'Python':", text.find("Python"))
