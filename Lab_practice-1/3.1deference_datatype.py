# Demonstrating different data types in Python

# Integer
age = 25
print(f"Age (Integer): {age}")

# Float
height = 5.9
print(f"Height (Float): {height}")

# String
name = "John Doe"
print(f"Name (String): {name}")

# Boolean
is_student = True
print(f"Is Student (Boolean): {is_student}")

# List
hobbies = ["Reading", "Traveling", "Gaming"]
print(f"Hobbies (List): {hobbies}")

# Tuple
coordinates = (10.5, 20.3)
print(f"Coordinates (Tuple): {coordinates}")

# Dictionary
person = {"name": "John", "age": 25, "city": "New York"}
print(f"Person Info (Dictionary): {person}")

# Set
unique_numbers = {1, 2, 3, 4, 5}
print(f"Unique Numbers (Set): {unique_numbers}")

