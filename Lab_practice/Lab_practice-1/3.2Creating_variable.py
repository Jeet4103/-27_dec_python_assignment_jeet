# Creating variables and assigning values with types

# Integer: Representing age
age = 25
print(f"Age: {age}, Type: {type(age)}")

# Float: Representing price
price = 49.99
print(f"Price: {price}, Type: {type(price)}")

# String: Representing a name
name = "Alice"
print(f"Name: {name}, Type: {type(name)}")

# Boolean: Representing a True/False value
is_student = True
print(f"Is Student: {is_student}, Type: {type(is_student)}")

# List: Representing multiple items
colors = ["Red", "Blue", "Green"]
print(f"Colors: {colors}, Type: {type(colors)}")

# Tuple: Representing immutable items
coordinates = (10.5, 20.7)
print(f"Coordinates: {coordinates}, Type: {type(coordinates)}")

# Dictionary: Representing key-value pairs
person = {"name": "Alice", "age": 25, "city": "Paris"}
print(f"Person Info: {person}, Type: {type(person)}")

# Set: Representing unique items
unique_numbers = {1, 2, 3, 4, 5}
print(f"Unique Numbers: {unique_numbers}, Type: {type(unique_numbers)}")

