text = "Hello, Python!"

print("Original string:", text)
print("Slice from index 0 to 5:", text[0:5])
print("Slice from index 7 to the end:", text[7:])
print("Slice the first 5 characters:", text[:5])
print("Slice the last 6 characters:", text[-6:])
print("Slice with step (every 2nd character):", text[::2])
print("Reverse the string:", text[::-1])
