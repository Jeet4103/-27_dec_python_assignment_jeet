# Creating a dictionary
my_dict = {
    "name": "Alice",
    "age": 25,
    "city": "New York",
    "job": "Engineer"
}

# Accessing values using dictionary keys
name = my_dict["name"]
age = my_dict["age"]
city = my_dict["city"]
job = my_dict["job"]

# Printing the values
print("Name:", name)
print("Age:", age)
print("City:", city)
print("Job:", job)
