# Integer List
integer_list = [1, 2, 3, 4, 5]
print("Integer List:", integer_list)

# String List
string_list = ["apple", "banana", "cherry", "date"]
print("String List:", string_list)

# Float List
float_list = [1.1, 2.2, 3.3, 4.4]
print("Float List:", float_list)

# Dictionary List
dict_list = [{"name": "Alice"}, {"name": "Bob"}, {"name": "Charlie"}]
print("Dictionary List:", dict_list)

# Tuple List
tuple_list = [(1, 2), (3, 4), (5, 6)]
print("Tuple List:", tuple_list)

